# Faça um algoritimo para somar dois números e multiplicar o resultado pelo primeiro número.

#Entradas
num1 = int(input("Informe o primeiro número: "))
num2 = int(input("Informe o segundo número: "))

#Processamento
soma = num1 + num2
multipicação = soma * num1

#Saida
print("O resultado da multiplicação é {0}". format(multipicação))